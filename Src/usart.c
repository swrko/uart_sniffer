  USART_InitStruct.BaudRate = parameters.baudrate;
  USART_InitStruct.DataWidth = parameters.datawidth;
  USART_InitStruct.StopBits = parameters.stopbit;
  USART_InitStruct.Parity = LL_USART_PARITY_NONE;
