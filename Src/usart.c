/* USER CODE BEGIN 1 */
void rx_usart1(uint8_t ch)
{
	if (usart1_callback != 0)
	{
		usart1_callback(ch);
	}
}
void rx_usart2(uint8_t ch)
{
	if (usart2_callback != 0)
	{
		usart2_callback(ch);
	}
}
